function fahrzeuge.load()
	for _,v in ipairs(fahrzeuge)do
		fahrzeuge.vehicle = createVehicle(v[1],v[2],v[3],v[4],v[5],v[6],v[7]);
		setElementData(fahrzeuge.vehicle,"level",v[8]);
		setElementFrozen(fahrzeuge.vehicle,true);
		setVehicleColor(fahrzeuge.vehicle,255,255,255);
		setVehicleDamageProof(fahrzeuge.vehicle,true);
		
		addEventHandler("onVehicleStartEnter",fahrzeuge.vehicle,function(player)
			if(tonumber(getElementData(player,"level")) >= tonumber(getElementData(source,"level")))then
				setElementFrozen(source,false);
			else
				infobox.create(player,language[language.get(player)][3][1]..getElementData(source,"level")..language[language.get(player)][3][2],"error");
				cancelEvent();
			end
		end)
	end
end
fahrzeuge.load()

function fahrzeuge.respawn()
	fahrzeuge.timer = setTimer(function()
		for _,v in ipairs(getElementsByType("vehicle"))do
			if(not(getVehicleOccupant(v)))then
				respawnVehicle(v);
			end
		end
		setTimer(fahrzeuge.respawn,3600000,1);
	end,60000,1)
end
setTimer(fahrzeuge.respawn,3600000,1)