function language.change()
	if(isElement(GUIEditor.window[1]))then destroyElement(GUIEditor.window[1])end
	
    GUIEditor.window[1] = guiCreateWindow(0.30, 0.36, 0.24, 0.30, language[language.get()][10][2], true)

    GUIEditor.gridlist[1] = guiCreateGridList(0.03, 0.10, 0.94, 0.72, true, GUIEditor.window[1])
    sprache = guiGridListAddColumn(GUIEditor.gridlist[1]," ", 0.9)

    GUIEditor.button[1] = guiCreateButton(0.03, 0.86, 0.94, 0.11, language[language.get()][10][3], true, GUIEditor.window[1])
	
	GUIEditor.set("set");
	
	for key,_ in pairs(language["List"])do
		row = guiGridListAddRow(GUIEditor.gridlist[1])
		guiGridListSetItemText(GUIEditor.gridlist[1],row,sprache,language["List"][key],false,false)
	end
	
	addEventHandler("onClientGUIClick",GUIEditor.button[1],function()
		local sprache = guiGridListGetItemText(GUIEditor.gridlist[1],guiGridListGetSelectedItem(GUIEditor.gridlist[1]),1)
					
		if(not(sprache == ""))then
			setElementData(localPlayer,"language",language["Sprachen"][sprache]);
			infobox.create(language[language.get()][10][4]..sprache..language[language.get()][10][5],"info");
			
			guiSetText(GUIEditor.window[1],language[language.get()][10][2]);
			guiSetText(GUIEditor.button[1],language[language.get()][10][3]);
			
			for i = 1,5 do
				anmeldebereich.texts[i] = language[language.get()][2][i];
			end
		else infobox.create(language[language.get()][10][1],"error")end
	end,false)
end

function language.get()
	return getElementData(localPlayer,"language");
end
